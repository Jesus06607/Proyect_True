    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Apartamos un contador para ver si hay resultados
        int contador=0;
        String clave="", nombre="", contra="", estatus="", telefono="", correo="", sexo="";
        //A partir de aqu√≠, despu√©s de introducir el c√≥digo, checamos la existencia en la base de datos con el controller.
        ResultSet rs1 = Controllers.mostrardatos(" Usuarios ", " clave, Nombre, Contrase√±a, Estatus, Telefono, Correo, Sexo ", " clave = '"+txt_Buscar.getText()+"'" );
        try {
            while(rs1.next()){
                contador++;
                clave       = rs1.getString("clave");
                nombre      = rs1.getString("Nombre");
                contra      = rs1.getString("Contrase√±a");
                estatus     = rs1.getString("Estatus");
                telefono    = rs1.getString("Telefono");
                correo      = rs1.getString("Correo");
                sexo        = rs1.getString("Sexo");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Mod_Users.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(contador>0){
            txt_clave.setText(clave);
            txt_nombre.setText(nombre);
            txt_contra.setText(contra);
            txt_status.setText(estatus);
            txt_telefono.setText(telefono);
            txt_correo.setText(correo);
            txt_sexo.setText(sexo);
        }else{
            System.out.println("No hay");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

